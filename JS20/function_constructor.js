// Standard Naming convention for Constructor Function
let Person = function (name, age, job) {
    this.name = name;
    this.age = age;
    this.job = job;
}

// Can also be used like this but not a Standard Naming convention for Constructor Function
let Student = function (nameArg, ageArg, programArg) {
    this.name = nameArg;
    this.age = ageArg;
    this.program = programArg;
}

